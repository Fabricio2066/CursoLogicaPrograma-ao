# Peça 4 notas de um aluno e calcule a media

NomeAluno = input("Digite o nome: ")

Nota1 = float( input("Digite a nota: "))
Nota2 = float( input("Digite a nota: "))
Nota3 = float( input("Digite a nota: "))
Nota4 = float( input("Digite a nota: "))

Resultado = float( Nota1 + Nota2 + Nota3 + Nota4)

print("\n")

print("A media do aluno(a)", NomeAluno, "é:", Resultado / 4)